let quantidadeDeProdutos
let preçoDeCadaUnidade
let ValorTotalDeUmaCompra

quantidadeDeProdutos = prompt("informe a quantidade de Produtos")

preçoDeCadaUnidade = prompt("informe o preço de cada unidade")

quantidadeDeProdutos = parseFloat (quantidadeDeProdutos)
preçoDeCadaUnidade = parseFloat(preçoDeCadaUnidade)

ValorTotalDeUmaCompra = preçoDeCadaUnidade * quantidadeDeProdutos

