let médiaDeUmAluno
let primeiraAvaliaçao
let segundaAvalição
let terceiraAvalição

primeiraAvaliaçao = prompt("informe os pontos da primeira avalição")

segundaAvalição = prompt ("infomre os pontos da segunda avaliação")

terceiraAvalição = prompt ("informe os pontos da terceira avaliação")

primeiraAvaliaçao = parseFloat(primeiraAvaliaçao)
segundaAvalição = parseFloat(segundaAvalição)
terceiraAvalição = parseFloat(terceiraAvalição)

médiaDeUmAluno = (primeiraAvaliaçao + segundaAvalição + terceiraAvalição) / 3